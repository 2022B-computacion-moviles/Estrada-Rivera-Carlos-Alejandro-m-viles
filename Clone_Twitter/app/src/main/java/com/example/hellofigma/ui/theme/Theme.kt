package com.example.hellofigma.ui.theme

import androidx.compose.foundation.isSystemInDarkTheme
import androidx.compose.material.MaterialTheme
import androidx.compose.material.darkColors
import androidx.compose.material.lightColors
import androidx.compose.runtime.Composable
import androidx.compose.ui.graphics.Color
val Twitter = Color(0xFF4C9EEB)
val White = Color(0xFFFFFFFF)
val Black = Color(0xFF5D6168)
val Transparent = Color(0xFFFFFF)
private val DarkColorPalette = darkColors(
    primary = White,
    primaryVariant = Twitter,
    secondary = White
)

private val LightColorPalette = lightColors(
    primary = White,
    primaryVariant = Twitter,
    secondary = Black
)

@Composable
fun TwitterTheme(darkTheme: Boolean = isSystemInDarkTheme(), content: @Composable () -> Unit) {
    val colors = if (darkTheme) {
        DarkColorPalette
    } else {
        LightColorPalette
    }

    MaterialTheme(
        colors = colors,
        typography = Typography,
        shapes = Shapes,
        content = content
    )
}